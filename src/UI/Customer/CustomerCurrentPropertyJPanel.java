    public void populate(){
        
        Customer c = this.network.getCustomerDirectory().findCustomer(user.getUserId());
        if(c.getProperty().getID() == null){
            JOptionPane.showMessageDialog(null, "You do not have any properties yet. But you can rent one right away!! :) ");

        }
        else{
            tableModel.setRowCount(0);

                Object[] row = new Object[10];

                row[0] = c.getProperty();
                row[1] = c.getProperty().getNo_of_rooms();
                row[2] = c.getProperty().getNo_of_bathrooms();
                row[3] = c.getProperty().getFull_address();
                row[4] = c.getProperty().getPrice();
                row[5] = c.getProperty().getLease().getStart_date();
                row[6] = c.getProperty().getLease().getType();
                row[7] = c.getProperty().getLease().getIsLeaseSigned();

                tableModel.addRow(row);


            fieldTotalRentPrice.setText(String.valueOf(c.getRentalsTotal()));           
        }

    }
    
    private void formatRows() {

        jTable2.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                final Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                String status = String.valueOf(tableModel.getValueAt(row, 7));
                c.setBackground(status.equals("True") ? Color.GREEN : Color.WHITE);
                return c;
            }
        });

    }

