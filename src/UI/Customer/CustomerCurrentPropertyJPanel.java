    private void LeaseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LeaseBtnActionPerformed
        // TODO add your handling code here:
        Customer c = this.network.getCustomerDirectory().findCustomer(user.getUserId());
        JFileChooser fileChooser = new JFileChooser();
        
        int response = fileChooser.showOpenDialog(this);
        
        if (response == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            String fileName = selectedFile.getName();

            // Save file to project directory
            String projectDirectory = System.getProperty("user.dir");
//            System.out.println(projectDirectory);
            Path sourcePath = selectedFile.toPath();
            Path targetPath = new File(projectDirectory + "/src/LeaseAgreements/" + fileName).toPath();
//            System.out.println(targetPath);
            try {
                Files.copy(sourcePath, targetPath, StandardCopyOption.REPLACE_EXISTING);
                JOptionPane.showMessageDialog(null, "File saved to System!");
                
                // Saving the path of the lease document as a string to the Lease doc variable of class 'Lease'. 
                // This pertains to specific customer.
                c.getProperty().getLease().setLeaseDoc(String.valueOf(targetPath));
                
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }       
        
    }//GEN-LAST:event_LeaseBtnActionPerformed

