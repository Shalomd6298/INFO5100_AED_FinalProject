    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        Customer c = this.network.getCustomerDirectory().findCustomer(user.getUserId());
        
        int selectedrow = jTable2.getSelectedRow();

        if(selectedrow >= 0){
            this.selectedBooking = (String) jTable2.getValueAt(selectedrow, 0);
            for(Room r : this.network.getRoomDirectory().getRooms()){
                Booking booking = r.findBookingByID(selectedBooking);
                if(booking != null){
                   for(Booking b : r.getBookings()){
                       if(b.getID().equals(booking.getID())){
                           r.getBookings().remove(b);
                           break;
                       }
                   }                   
                }
            }    
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row ");
        } 
        
        populate();
        
    }//GEN-LAST:event_jButton1ActionPerformed


