    public void populate() {
        
        Customer c = this.network.getCustomerDirectory().findCustomer(user.getUserId());

        tableModel2.setRowCount(0);
        for(Room r : this.network.getRoomDirectory().getRooms()){
            for(Booking b : r.getBookings()){
                if(b.getCustomer().getUserId().equals(c.getUserId())){
                   Object[] row = new Object[10];

                   row[0] = b.getID();
                   row[1] = r.getRoomID();
                   row[2] = b.getCustomer().getUserId();
                   row[3] = b.getCustomer().getFullName();
                   row[4] = b.getDate();
                   row[5] = b.getStart_time();
                   row[6] = b.getEnd_time();

                    tableModel2.addRow(row);                  
                }

            }
        }
    }

