    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        int selectedrow = jTable1.getSelectedRow();
 
        if(selectedrow >= 0){
            if (fieldupdateExp.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Cannot input a null value. Please enter no of years !");
            }else{
                Float exp = Float.valueOf(fieldupdateExp.getText());
                this.selectedEmployee = (EmployerProfile) jTable1.getValueAt(selectedrow, 0);
                this.selectedEmployee.setExperience(exp); 
            }
 
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row ");
        }             
        
        fieldupdateExp.setText("");
        

   
        populate();
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        if(fieldCreateID.getText().isEmpty() || fieldCreateFullname.getText().isEmpty() || fieldCreateExp.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Cannot input a null value. Please enter all the details !");
        }else{
        
            String ID = fieldCreateID.getText();
            String fullname = fieldCreateFullname.getText();
            String role = (String) jComboBox1.getSelectedItem();
            Float exp = Float.valueOf(fieldCreateExp.getText());

            this.network.getEmployerDirectory().newEmployerProfile(ID, fullname, role, exp);  
            
            fieldCreateID.setText("");
            fieldCreateFullname.setText("");
            fieldCreateExp.setText("");
        }
        
        populate();        
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int selectedrow = jTable1.getSelectedRow();

        if(selectedrow >= 0){
            this.selectedEmployee = (EmployerProfile) jTable1.getValueAt(selectedrow, 0);
            this.network.getEmployerDirectory().getEmployerlist().remove(selectedEmployee);
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row ");
        }  
        
        populate();     
        
    }//GEN-LAST:event_jButton1ActionPerformed

