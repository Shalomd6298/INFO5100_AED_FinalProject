    public void populate(){
        
        tableModel.setRowCount(0);

        for(EmployerProfile ep : this.network.getEmployerDirectory().getEmployerlist()){
            Object[] row = new Object[10];

            row[0] = ep;
            row[1] = ep.getFullname();
            row[2] = ep.getRole();
            row[3] = ep.getExperience() ;

            tableModel.addRow(row);

        }    
    }
    

