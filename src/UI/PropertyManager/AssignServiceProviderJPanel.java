    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ServiceRequestDirectory srd = this.network.getServicerequestdirectory();
        
        int selectedServiceRequest = jTable1.getSelectedRow();
        String servicerequesID = tableModel1.getValueAt(selectedServiceRequest, 0).toString();
        
        String selectedServiceProviderID = jComboBox1.getSelectedItem().toString();
        ServiceProvider selectedserviceprovider = network.getServiceproviderdir().findServiceProviderbyName(selectedServiceProviderID);
        
        if(jComboBox1.getSelectedItem() != null) {
            
            if(selectedServiceRequest <= 0) {
                
                for (ServiceRequest sr : srd.getServicerequestlist()){
                   
                    if(srd.findServiceReqByID(servicerequesID) != null){
                        ServiceRequest nsr = srd.findServiceReqByID(servicerequesID);
                        nsr.setServiceprovider(selectedserviceprovider);
                        nsr.setService_status("Service Provider Assigned");
                   }
                    populateRequest();
                    populateAssignedList();
                }
                
            } else {
                JOptionPane.showMessageDialog(this, "Please Selected the Priority Service request");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Select Valid Service provider");
        }

        for(ServiceRequest sr : this.network.getServicerequestdirectory().getServicerequestlist()){
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed


