    public void populateRequest(){
        tableModel1.setRowCount(0);
        // System.out.println(this.network.getPropertyDirectory().getProperties().size());
        for(ServiceRequest sr : this.network.getServicerequestdirectory().getServicerequestlist()){
            
            if (sr.getServiceprovider() == null){
                    Object[] row = new Object[4];

                    row[0] = sr;
                    row[1] = sr.getService_type();
                    row[2] = sr.getService_subject();
                    row[3] = sr.getCustomer().getFullName();

                    tableModel1.addRow(row);
            }                         
        }
    }
    
    public void populateAssignedList(){
        tableModel2.setRowCount(0);
        // System.out.println(this.network.getPropertyDirectory().getProperties().size());
        for(ServiceRequest sr : this.network.getServicerequestdirectory().getServicerequestlist()){
            if(sr.getServiceprovider() != null){
                Object[] row = new Object[6];

                    row[0] = sr;
                    row[1] = sr.getService_type();
                    row[2] = sr.getServiceprovider().getFullName();
                    row[3] = sr.getCustomer().getFullName();
                    row[4] = sr.getService_price();
                    row[5] = sr.getService_status();

                    tableModel2.addRow(row);
            }
        }
    }
    
    public void serviceProviders() {
        MutableComboBoxModel<String> model = new DefaultComboBoxModel<String>();
        
        for (ServiceProvider sp : network.getServiceproviderdir().getServiceProviderList()) {
            model.addElement(sp.getFullName());
        }
        
        jComboBox1.setModel(model);
    }
    
