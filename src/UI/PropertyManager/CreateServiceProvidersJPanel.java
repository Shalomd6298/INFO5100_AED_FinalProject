    public void populateTable(){
        
        tableModel1.setRowCount(0);
        ServiceProviderDirectory spd = this.network.getServiceproviderdir();
        
        for(ServiceProvider sp : spd.getServiceProviderList()){
            Object row[] = new Object[8];
            row[0] = sp.getUserId().toString();
            row[1] = sp.getRole();
            row[2] = sp.getFullName();
            row[3] = sp.getUserName();
            row[4] = sp.getPassword();
            row[5] = sp.getPhoneNumber();
            row[6] = sp.getEmailId();
            row[7] = sp.getAge();
            
            tableModel1.addRow(row);
        }
        
    }
