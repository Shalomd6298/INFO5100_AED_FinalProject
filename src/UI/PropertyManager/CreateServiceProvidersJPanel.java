    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        //        // TODO add your handling code here:

        UUID uuid = UUID.randomUUID();
        String randomUUIDString = uuid.toString();

        String firstName = txtFName.getText();
        String lastName = txtLName.getText();
        String userName = txtUserName.getText();
        String password = txtPassword.getText();
        Integer age = Integer.valueOf(txtAge.getText());
        Long phoneNumber = Long.valueOf(txtPhoneNumber.getText());
        String userId = randomUUIDString;
        String emailAddress = txtEmailAddress.getText();
        String role = roleSelectionjComboBox.getSelectedItem().toString();

        if (firstName.isEmpty() || lastName.isEmpty() || userName.isEmpty()|| password.isEmpty()
            || txtAge.getText().isEmpty()|| userId.isEmpty()|| txtPhoneNumber.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter all details !");
            clearAllFields();
            return;
        }

        if (validatePhoneNumber(txtPhoneNumber.getText()) == null) {
            JOptionPane.showMessageDialog(this, "Please enter a valid phone number !");
            return;
        }

        if(validateEmailAddress(emailAddress) == null) {
            JOptionPane.showMessageDialog(this, "Please enter a valid email address !");
            return;
        }

        if (validateAge(txtAge.getText()) == null) {
            JOptionPane.showMessageDialog(this, "Please enter a valid age!");
            return;
        }

        this.userDirectory.createUserAccount(userName, password, role, userId);
        ServiceProviderDirectory ServiceProviderDirectory = this.network.getServiceproviderdir();
        
        ServiceProviderDirectory.createServiceProvider(userId, firstName, lastName, userName, password, age, emailAddress, phoneNumber, role);
        System.out.println("UD size : "  + this.userDirectory.getUsers().size());
        System.out.println("ED size : "  + ServiceProviderDirectory.getServiceProviderList().size());

        clearAllFields();
        populateTable();
    }//GEN-LAST:event_btnRegisterActionPerformed

