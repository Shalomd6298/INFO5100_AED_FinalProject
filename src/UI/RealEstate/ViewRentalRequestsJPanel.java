    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int selectedrow = jTable1.getSelectedRow();

        if(selectedrow >= 0){
            this.selectedRequest = (RentalRequest) jTable1.getValueAt(selectedrow, 0);
            this.selectedRequest.getProperty().setIsAvailable(false);
            this.selectedRequest.setApplication_status("Accepted");
//            this.selectedRequest.getCustomer().addOrder(this.selectedRequest);
            Float r = this.selectedRequest.getCustomer().calculateRentPrice(this.selectedRequest.getLease().getType(), this.selectedRequest.getProperty().getPrice()); 
            this.selectedRequest.getCustomer().setRentalsTotal(r);  
            this.selectedRequest.getCustomer().setProperty(this.selectedRequest.getProperty());
            JOptionPane.showMessageDialog(null, "This request has been Accepted! Customer will be notified. ");
            
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row ");
        }  
        
        populate();
        
    }//GEN-LAST:event_jButton1ActionPerformed

