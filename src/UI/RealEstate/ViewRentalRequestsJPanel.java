    public void populate() {
        
        tableModel.setRowCount(0);
        
        for(RentalRequest r : this.network.getRentalRequestDirectory().getRentalList()){
                
                Object[] row = new Object[10];

                row[0] = r;
                row[1] = r.getCustomer().getFullName();
                row[2] = r.getCustomer().getAge();
                row[3] = r.getCustomer().getEmailId();
                row[4] = r.getCustomer().getPhoneNumber();
                row[5] = r.getProperty().getID();
                row[6] = r.getProperty().getFull_address();
                row[7] = r.getLease().getType();  
                row[8] = String.valueOf(r.getProperty().getIsAvailable());
                row[9] = r.getApplication_status();
                
                tableModel.addRow(row);                

        }
    }
    
    private void formatRows() {

        jTable1.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                final Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                String status = String.valueOf(tableModel.getValueAt(row, 9));
                c.setBackground(status.equals("Application in Progress") ? Color.GRAY : status.equals("Accepted") ? Color.GREEN : Color.RED);
                return c;
            }
        });

    }
 
