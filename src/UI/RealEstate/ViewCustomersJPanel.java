    public void populate() {
        
        tableModel.setRowCount(0);
        for(Customer c : this.network.getCustomerDirectory().getCustomerlist()){
            for(RentalRequest r : c.getCustomerRentalList()){
                if(r.getApplication_status() == null){
                
                }else if(r.getApplication_status().equals("Accepted")){
                    
                   Object[] row = new Object[10];

                   row[0] = c.getProperty();
                   row[1] = c.getProperty().getFull_address(); 
                   row[2] = c; 
                   row[3] = c.getFirstName() + c.getLastName(); 
                   row[4] = c.getAge(); 
                   row[5] = c.getEmailId();
                   row[6] = c.getPhoneNumber();
                   row[7] = c.getProperty().getLease().getIsLeaseSigned();
                   row[8] = c.getProperty().getLease().getType();

                   tableModel.addRow(row);                      
                }
            }
        }
    }

