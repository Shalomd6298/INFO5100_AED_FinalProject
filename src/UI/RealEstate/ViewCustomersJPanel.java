    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int selectedrow = jTable1.getSelectedRow();

        if(selectedrow >= 0){
            this.selectedCustomer = (Customer) jTable1.getValueAt(selectedrow, 2);
            this.selectedCustomer.getProperty().getLease().setIsLeaseSigned(true);
            this.selectedCustomer.setActvity_status(true);
            
            CustomerBilling ct = new CustomerBilling();
            ct.setUserId(selectedCustomer.getUserId());
            ct.setFacilityUsed("Total Rent Amount");
            ct.setPrice(selectedCustomer.getRentalsTotal());
            this.network.getCustomerbillingdirectory().addCustomerBilling(ct);
            JOptionPane.showMessageDialog(null, "Lease contract approved! Customer will be notified.");
            
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row ");
        }  
        
        populate(); 
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        int selectedrow = jTable1.getSelectedRow();

        if(selectedrow >= 0){
            
            this.selectedCustomer = (Customer) jTable1.getValueAt(selectedrow, 2);
            
            try{
                File file = new File (selectedCustomer.getProperty().getLease().getLeaseDoc());
                if(file.exists()){
                    if(Desktop.isDesktopSupported()){
                        Desktop.getDesktop().open(file);
                    }else{
                        JOptionPane.showMessageDialog(this, "This file is not supported.");
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "This file does not exist.");
                }
            }catch(Exception e){
                 JOptionPane.showMessageDialog(this, "This file does not exist.");
//                e.printStackTrace();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row ");
        }  
        
    }//GEN-LAST:event_jButton2ActionPerformed


